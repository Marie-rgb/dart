//Switch Statement to Check Different String Values
void main() {
  String value = 'apple';

  switch (value) {
    case 'apple':
      print('Apple selected');
      break;
    case 'banana':
      print('Banana selected');
      break;
    case 'orange':
      print('Orange selected');
      break;
    default:
      print('Unknown fruit');
  }
}
